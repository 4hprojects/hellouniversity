function buildSeriesPageData(series) {
  const entries = series.entries.map((entry) => ({
    ...entry,
    href: `/books/${series.id}/${entry.slug}`,
    searchText: [
      series.title,
      series.eyebrow,
      series.domainLabel,
      entry.sequenceLabel,
      entry.linkTitle,
      entry.title,
      entry.teaser,
      entry.description
    ].join(' ').toLowerCase()
  }));

  const recommendedEntry = entries.find((entry) => entry.slug === series.recommendedSlug) || entries[0];
  const featuredEntries = entries.filter((entry) => series.featuredEntrySlugs.includes(entry.slug));
  const searchText = [
    series.title,
    series.summary,
    series.audience,
    series.domainLabel,
    series.level,
    ...entries.map((entry) => `${entry.sequenceLabel} ${entry.linkTitle} ${entry.teaser} ${entry.description}`)
  ].join(' ').toLowerCase();

  return {
    ...series,
    entries,
    entryCount: entries.length,
    recommendedEntry,
    featuredEntries,
    href: recommendedEntry ? recommendedEntry.href : `/books#series-${series.id}`,
    searchText
  };
}

const BOOK_SERIES_PAGE_DATA = BOOK_SERIES.map(buildSeriesPageData);

const BOOK_META = BOOK_SERIES_PAGE_DATA.reduce((accumulator, series) => {
  series.entries.forEach((entry) => {
    accumulator[`${series.id}/${entry.slug}`] = entry;
  });
  return accumulator;
}, {});

const FEATURED_BOOK_ENTRIES = [
  ['7-habits', 'scp1-be-proactive'],
  ['7-habits', 'scp7-sharpen-the-saw'],
  ['the-way-of-the-shepherd', 'principle1'],
  ['the-way-of-the-shepherd', 'principle4']
].map(([seriesId, slug]) => {
  const series = BOOK_SERIES_PAGE_DATA.find((item) => item.id === seriesId);
  const entry = series?.entries.find((item) => item.slug === slug);
  if (!series || !entry) {
    return null;
  }

  return {
    href: entry.href,
    title: entry.linkTitle,
    sequenceLabel: entry.sequenceLabel,
    seriesTitle: series.title,
    domainLabel: series.domainLabel,
    icon: series.icon,
    description: entry.teaser
  };
}).filter(Boolean);

const BOOK_ENTRY_SEARCH_INDEX = BOOK_SERIES_PAGE_DATA.flatMap((series) =>
  series.entries.map((entry) => ({
    slug: entry.slug,
    href: entry.href,
    title: entry.linkTitle,
    fullTitle: entry.title,
    sequenceLabel: entry.sequenceLabel,
    teaser: entry.teaser,
    description: entry.description,
    seriesId: series.id,
    seriesTitle: series.title,
    domain: series.domain,
    domainLabel: series.domainLabel,
    icon: series.icon,
    searchText: entry.searchText
  }))
);

function getBookMeta(series, slug) {
  return BOOK_META[`${series}/${slug}`] || null;
}

function getBookSeries(seriesId) {
  return BOOK_SERIES_PAGE_DATA.find((series) => series.id === seriesId) || null;
}

function getBooksPageData() {
  const seriesList = BOOK_SERIES_PAGE_DATA.slice();

  const seriesCount = seriesList.length;
  const entryCount = seriesList.reduce((sum, series) => sum + series.entryCount, 0);
  const leadershipEntryCount = seriesList
    .filter((series) => series.domain === 'leadership')
    .reduce((sum, series) => sum + series.entryCount, 0);
  const effectivenessEntryCount = seriesList
    .filter((series) => series.domain === 'effectiveness')
    .reduce((sum, series) => sum + series.entryCount, 0);

  return {
    bookSeries: seriesList,
    bookSearchEntries: BOOK_ENTRY_SEARCH_INDEX,
    featuredBookEntries: FEATURED_BOOK_ENTRIES,
    bookFilters: [
      { value: 'all', label: 'All Series' },
      { value: 'effectiveness', label: 'Personal Effectiveness' },
      { value: 'leadership', label: 'Leadership' }
    ],
    bookStats: {
      seriesCount,
      entryCount,
      leadershipEntryCount,
      effectivenessEntryCount
    }
  };
}

