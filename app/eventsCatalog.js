function clone(value) {
  return JSON.parse(JSON.stringify(value));
}

function buildSearchText(entry) {
  return [
    entry.title,
    entry.series,
    entry.typeLabel,
    entry.statusLabel,
    entry.dateLabel,
    entry.venue,
    entry.summary,
    entry.description || ''
  ].join(' ').toLowerCase();
}

function parseEntryDate(dateLabel, fallbackIndex) {
  const parsed = Date.parse(dateLabel);
  if (!Number.isNaN(parsed)) {
    return parsed;
  }

  return fallbackIndex * -1;
}

function sortEntriesNewestFirst(entries) {
  return entries
    .map((entry, index) => ({ entry, index }))
    .sort((left, right) => {
      const dateDifference =
        parseEntryDate(right.entry.dateLabel, right.index) - parseEntryDate(left.entry.dateLabel, left.index);

      if (dateDifference !== 0) {
        return dateDifference;
      }

      return left.index - right.index;
    })
    .map(({ entry }) => entry);
}

function normalizeDetailActionLabel(action) {
  const href = String(action.href || '').toLowerCase();
  const currentLabel = String(action.label || '').trim();

  if (href.includes('/events/') && href.includes('results')) {
    return 'Open results';
  }

  if (href.includes('/events/') && href.includes('info')) {
    return 'Open details';
  }

  if (href === '/events/2025bytefunrun' || href === '/events/itquizbee2025') {
    return 'Open registration';
  }

  if (/challenge website/i.test(currentLabel)) {
    return 'Challenge website';
  }

  return currentLabel.replace(/^view\s+/i, 'Open ');
}

function normalizeDetailActions(actions) {
  return (Array.isArray(actions) ? actions : [])
    .filter((action) => action && action.href && action.href !== '/events')
    .map((action) => ({
      ...action,
      label: normalizeDetailActionLabel(action)
    }))
    .slice(0, 2);
}

function normalizeEventEntry(entry) {
  return {
    slug: entry.slug,
    title: entry.title,
    series: entry.series,
    type: entry.type,
    typeLabel: entry.typeLabel,
    statusLabel: entry.statusLabel,
    dateLabel: entry.dateLabel,
    venue: entry.venue,
    href: entry.href,
    image: entry.image,
    summary: entry.summary,
    indexable: entry.indexable !== false,
    ctaLabel: 'Open page',
    searchText: buildSearchText(entry)
  };
}

function getRelatedEventLinks(currentSlug) {
  const page = eventPages.find((entry) => entry.slug === currentSlug);
  if (!page) {
    return [{ href: '/events', label: 'Events archive' }];
  }

  const sameSeriesLinks = eventPages
    .filter((entry) => entry.series === page.series && entry.slug !== currentSlug)
    .map((entry) => ({
      href: entry.href,
      label: entry.title
    }));

  return [
    ...sameSeriesLinks,
    { href: '/events', label: 'Events archive' }
  ];
}

function getEventPage(slug) {
  const page = eventPages.find((entry) => entry.slug === slug);
  if (!page) {
    return null;
  }

  const clonedPage = clone(page);
  clonedPage.actions = normalizeDetailActions(clonedPage.actions);
  clonedPage.breadcrumbs = [{ href: '/events', label: 'Events' }];
  clonedPage.relatedLinks = getRelatedEventLinks(slug);
  return clonedPage;
}

function hasEventPage(slug) {
  return eventPages.some((entry) => entry.slug === slug);
}

function getArchivedSubmissionPage(slug) {
  const page = archivedSubmissionPages.find((entry) => entry.slug === slug);
  return page ? clone(page) : null;
}

function hasArchivedSubmissionPage(slug) {
  return archivedSubmissionPages.some((entry) => entry.slug === slug);
}

